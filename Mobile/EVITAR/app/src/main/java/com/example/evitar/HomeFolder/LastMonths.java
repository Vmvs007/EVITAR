package com.example.evitar.HomeFolder;

public class LastMonths {

    private int mes;
    private int mes1;
    private int mes2;
    private int mes3;
    private int mes4;
    private int mes5;

    public LastMonths(int mes, int mes1, int mes2, int mes3, int mes4, int mes5) {
        this.mes = mes;
        this.mes1 = mes1;
        this.mes2 = mes2;
        this.mes3 = mes3;
        this.mes4 = mes4;
        this.mes5 = mes5;
    }

    public int getMes() {
        return mes;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public int getMes1() {
        return mes1;
    }

    public void setMes1(int mes1) {
        this.mes1 = mes1;
    }

    public int getMes2() {
        return mes2;
    }

    public void setMes2(int mes2) {
        this.mes2 = mes2;
    }

    public int getMes3() {
        return mes3;
    }

    public void setMes3(int mes3) {
        this.mes3 = mes3;
    }

    public int getMes4() {
        return mes4;
    }

    public void setMes4(int mes4) {
        this.mes4 = mes4;
    }

    public int getMes5() {
        return mes5;
    }

    public void setMes5(int mes5) {
        this.mes5 = mes5;
    }
}
